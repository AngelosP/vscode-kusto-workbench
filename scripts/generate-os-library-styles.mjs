/**
 * Generates src/webview/shared/os-library-styles.ts from the OverlayScrollbars
 * minified CSS. Run during build (esbuild.js calls this).
 *
 * Usage: node scripts/generate-os-library-styles.mjs
 */
import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');

const cssPath = join(root, 'node_modules', 'overlayscrollbars', 'styles', 'overlayscrollbars.min.css');
const outPath = join(root, 'src', 'webview', 'shared', 'os-library-styles.ts');

const css = readFileSync(cssPath, 'utf8');

const ts = `/**
 * OverlayScrollbars library CSS as a constructable CSSStyleSheet.
 *
 * AUTO-GENERATED by scripts/generate-os-library-styles.mjs — do not edit.
 *
 * This allows Shadow DOM components to adopt the library's structural CSS
 * (scrollbar hiding via scrollbar-width:none, positioning, visibility, etc.)
 * which cannot penetrate shadow boundaries from the document-level <style> tag.
 *
 * Usage:
 *   import { osLibrarySheet } from '../shared/os-library-styles.js';
 *   static styles = [osLibrarySheet, osThemeSheet, styles];
 */

const css = ${JSON.stringify(css)};

const sheet = new CSSStyleSheet();
sheet.replaceSync(css);
export const osLibrarySheet: CSSStyleSheet = sheet;
`;

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, ts, 'utf8');
console.log(`[os-library-styles] Generated ${outPath} (${css.length} bytes CSS)`);
